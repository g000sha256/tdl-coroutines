package dev.g000sha256.tdl.dto

import kotlin.Any
import kotlin.Boolean
import kotlin.Int
import kotlin.String

/**
 * This class is an abstract base class.
 * Describes the order of the found affiliate programs.
 */
public sealed class AffiliateProgramSortOrderDto private constructor() {
    /**
     * The affiliate programs must be sorted by the profitability.
     */
    public class Profitability public constructor() : AffiliateProgramSortOrderDto() {
        override fun equals(other: Any?): Boolean {
            if (other === this) {
                return true
            }
            if (other == null) {
                return false
            }
            return this::class == other::class
        }

        override fun hashCode(): Int {
            return this::class.hashCode()
        }

        override fun toString(): String {
            return "AffiliateProgramSortOrderDto.Profitability()"
        }
    }

    /**
     * The affiliate programs must be sorted by creation date.
     */
    public class CreationDate public constructor() : AffiliateProgramSortOrderDto() {
        override fun equals(other: Any?): Boolean {
            if (other === this) {
                return true
            }
            if (other == null) {
                return false
            }
            return this::class == other::class
        }

        override fun hashCode(): Int {
            return this::class.hashCode()
        }

        override fun toString(): String {
            return "AffiliateProgramSortOrderDto.CreationDate()"
        }
    }

    /**
     * The affiliate programs must be sorted by the expected revenue.
     */
    public class Revenue public constructor() : AffiliateProgramSortOrderDto() {
        override fun equals(other: Any?): Boolean {
            if (other === this) {
                return true
            }
            if (other == null) {
                return false
            }
            return this::class == other::class
        }

        override fun hashCode(): Int {
            return this::class.hashCode()
        }

        override fun toString(): String {
            return "AffiliateProgramSortOrderDto.Revenue()"
        }
    }
}
