package dev.g000sha256.tdl.dto

import kotlin.Any
import kotlin.Boolean
import kotlin.Int
import kotlin.String

/**
 * This class is an abstract base class.
 * Describes type of sticker.
 */
public sealed class StickerTypeDto private constructor() {
    /**
     * The sticker is a regular sticker.
     */
    public class Regular public constructor() : StickerTypeDto() {
        override fun equals(other: Any?): Boolean {
            if (other === this) {
                return true
            }
            if (other == null) {
                return false
            }
            return this::class == other::class
        }

        override fun hashCode(): Int {
            return this::class.hashCode()
        }

        override fun toString(): String {
            return "StickerTypeDto.Regular()"
        }
    }

    /**
     * The sticker is a mask in WEBP format to be placed on photos or videos.
     */
    public class Mask public constructor() : StickerTypeDto() {
        override fun equals(other: Any?): Boolean {
            if (other === this) {
                return true
            }
            if (other == null) {
                return false
            }
            return this::class == other::class
        }

        override fun hashCode(): Int {
            return this::class.hashCode()
        }

        override fun toString(): String {
            return "StickerTypeDto.Mask()"
        }
    }

    /**
     * The sticker is a custom emoji to be used inside message text and caption.
     */
    public class CustomEmoji public constructor() : StickerTypeDto() {
        override fun equals(other: Any?): Boolean {
            if (other === this) {
                return true
            }
            if (other == null) {
                return false
            }
            return this::class == other::class
        }

        override fun hashCode(): Int {
            return this::class.hashCode()
        }

        override fun toString(): String {
            return "StickerTypeDto.CustomEmoji()"
        }
    }
}
