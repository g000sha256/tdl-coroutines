package dev.g000sha256.tdl.dto

import kotlin.Any
import kotlin.Boolean
import kotlin.Int
import kotlin.String

/**
 * This class is an abstract base class.
 * Describes sources of reactions for which notifications will be shown.
 */
public sealed class ReactionNotificationSourceDto private constructor() {
    /**
     * Notifications for reactions are disabled.
     */
    public class None public constructor() : ReactionNotificationSourceDto() {
        override fun equals(other: Any?): Boolean {
            if (other === this) {
                return true
            }
            if (other == null) {
                return false
            }
            return this::class == other::class
        }

        override fun hashCode(): Int {
            return this::class.hashCode()
        }

        override fun toString(): String {
            return "ReactionNotificationSourceDto.None()"
        }
    }

    /**
     * Notifications for reactions are shown only for reactions from contacts.
     */
    public class Contacts public constructor() : ReactionNotificationSourceDto() {
        override fun equals(other: Any?): Boolean {
            if (other === this) {
                return true
            }
            if (other == null) {
                return false
            }
            return this::class == other::class
        }

        override fun hashCode(): Int {
            return this::class.hashCode()
        }

        override fun toString(): String {
            return "ReactionNotificationSourceDto.Contacts()"
        }
    }

    /**
     * Notifications for reactions are shown for all reactions.
     */
    public class All public constructor() : ReactionNotificationSourceDto() {
        override fun equals(other: Any?): Boolean {
            if (other === this) {
                return true
            }
            if (other == null) {
                return false
            }
            return this::class == other::class
        }

        override fun hashCode(): Int {
            return this::class.hashCode()
        }

        override fun toString(): String {
            return "ReactionNotificationSourceDto.All()"
        }
    }
}
