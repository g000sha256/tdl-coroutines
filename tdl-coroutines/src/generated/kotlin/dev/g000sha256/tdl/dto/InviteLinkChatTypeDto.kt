package dev.g000sha256.tdl.dto

import kotlin.Any
import kotlin.Boolean
import kotlin.Int
import kotlin.String

/**
 * This class is an abstract base class.
 * Describes the type of chat to which points an invite link.
 */
public sealed class InviteLinkChatTypeDto private constructor() {
    /**
     * The link is an invite link for a basic group.
     */
    public class BasicGroup public constructor() : InviteLinkChatTypeDto() {
        override fun equals(other: Any?): Boolean {
            if (other === this) {
                return true
            }
            if (other == null) {
                return false
            }
            return this::class == other::class
        }

        override fun hashCode(): Int {
            return this::class.hashCode()
        }

        override fun toString(): String {
            return "InviteLinkChatTypeDto.BasicGroup()"
        }
    }

    /**
     * The link is an invite link for a supergroup.
     */
    public class Supergroup public constructor() : InviteLinkChatTypeDto() {
        override fun equals(other: Any?): Boolean {
            if (other === this) {
                return true
            }
            if (other == null) {
                return false
            }
            return this::class == other::class
        }

        override fun hashCode(): Int {
            return this::class.hashCode()
        }

        override fun toString(): String {
            return "InviteLinkChatTypeDto.Supergroup()"
        }
    }

    /**
     * The link is an invite link for a channel.
     */
    public class Channel public constructor() : InviteLinkChatTypeDto() {
        override fun equals(other: Any?): Boolean {
            if (other === this) {
                return true
            }
            if (other == null) {
                return false
            }
            return this::class == other::class
        }

        override fun hashCode(): Int {
            return this::class.hashCode()
        }

        override fun toString(): String {
            return "InviteLinkChatTypeDto.Channel()"
        }
    }
}
